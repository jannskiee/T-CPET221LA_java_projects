package no3;

public class Sales {
	private double price;
	private double discountRate;
	
	public void setSale(double a, double b) {
		price = a;
		discountRate = b;
	}
	
	public double getPrice() {
		return price;
		
	}
	
	public double getDiscountRate() {
		return discountRate;
	}

}
